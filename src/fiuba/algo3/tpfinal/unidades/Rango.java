package fiuba.algo3.tpfinal.unidades;

public class Rango {
	
	private int rangoTierra;
	private int rangoAire;

	public Rango(int tierra, int aire){
		this.rangoTierra = tierra;
		this.rangoAire = aire;
	}

	public int getRangoTierra() {
		return rangoTierra;
	}

	
	public int getRangoAire() {
		return rangoAire;
	}

	
}
